#Create a JSON of all object types and import the JSON into a SQL Database

import json
print('Json Dict:',json.dumps({"name": "Ravi", "age": 30,"Place":"Chennai"}))
print('Json List:',json.dumps(["apple", "bananas"]))
print('Json-tuple:',json.dumps(("apple", "bananas")))
print('Json String:',json.dumps("hello"))
print('Json Int:',json.dumps(10))
print('Json Float:',json.dumps(31.56))
print('Json True:',json.dumps(True))
print('Json False:',json.dumps(False))
print('Json none:',json.dumps(None))
    

